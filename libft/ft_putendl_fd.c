#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	c;

	if (s)
	{
		while (*s != '\0')
		{
			c = *s;
			write(fd, &c, 1);
			s++;
		}
		write(fd, "\n", 1);
	}
}
